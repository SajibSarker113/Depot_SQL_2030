BREAK ON REPORT SKIP-2

TTITLE CENTER 'DEPOT: '&&SC_CD'.' SKIP 1 -
CENTER 'OUTSTANDING STATEMENT' SKIP 1 CENTER ''AS- ON: '&&LAST_DELI_DATE' -
RIGHT 'MORE THAN DAYS' SKIP 2

